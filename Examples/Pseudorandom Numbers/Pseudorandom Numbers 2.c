#include <stdio.h>  //For printf and scanf.
#include <stdlib.h> //New #include for srand and rand.
#include <time.h>   //For time.

int main()
{
    srand(time(NULL));
    int n = 5, max, min;

    srand(time(NULL));
    printf("What range of values would you like to create? ");
    scanf("%d %d", &min, &max);

    for (int i = 0; i < n; i++)
    {
        int randomNum = rand() % (max + 1 - min) + min;
        printf("%d  ", randomNum);
    }

    printf("\n");

    return 0;
}