#include <stdio.h> //For printf and scanf.
#include <stdlib.h> //New #include for srand and rand.
#include <time.h> //For time.

int main()
{

    int n;
    
    srand(time(NULL));
    printf("How many numbers would you like to generate? ");
    scanf("%d", &n);

    for (int i = 0; i < n; i++)
    {
        int randomNum = rand();
        printf("%d  ", randomNum);
    }

    printf("\n");

    return 0;
}