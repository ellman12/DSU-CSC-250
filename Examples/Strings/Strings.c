#include <stdio.h>

int main()
{

    //Null terminator '\0'.
    // char array[5][10] = {"Austin", "Rose", "Sean", "Owen", "Theia"}; //Professor's family.
    char array[5][10];

    for (int i = 0; i < 5; i++)
    {

        scanf("%s", array[i]);
    }

    printf("\n");

    for (int i = 0; i < 5; i++)
    {
        printf("%s\n", array[i]);
    }

    return 0;
}