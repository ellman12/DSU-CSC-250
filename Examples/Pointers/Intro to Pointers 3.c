#include <stdio.h>

int main()
{
    int x, y;
    double *ptr = &x;

    printf("Please enter in a value for x: ");
    scanf("%d", ptr);

    double db = *ptr + 10;

    printf("db is equal to %lf, and x is equal to %d.\n", db, x);

    printf("The value of &x is %p, ptr=%p, &ptr=%p, *ptr=%d\n", &x, ptr, &ptr, *ptr);
    return 0;
}