#include <stdio.h>

int main()
{
    int x, y;
    int *ptr = &x;
    int *ptr2 = &x;

    printf("Please enter in a value for x: ");
    scanf("%d", ptr);

    printf("You entered in %d.\n", *ptr2);

    printf("The value of &x is %p, ptr=%p, &ptr=%p, *ptr=%d\n", &x, ptr, &ptr, *ptr);
    return 0;
}