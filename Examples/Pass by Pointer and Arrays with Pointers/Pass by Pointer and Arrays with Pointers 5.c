#include <stdio.h>

int main()
{
    int x, y = 10;
    printf("&x = %p, &y = %p\n", &x, &y);
    printf("y = %d\n", *(&x-1));

    *(&x - 1) = 42;
    printf("y = %d\n", y);

    return 0;
}