#include <stdio.h>
#include <stdlib.h>

int main()
{
    FILE *binFilePtr = fopen("balances.bin", "rb");

    float x;
    while ((fread(&x, sizeof(float), 1, binFilePtr)) != 0)
    {
        printf("x: %f\n", x);
    }

    fclose(binFilePtr);

    return 0;
}