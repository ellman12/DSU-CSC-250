#include <stdio.h>
#include <stdlib.h>

int main()
{
    FILE *fp = fopen("testnumbers.dat", "w+");

    for (int i = 0; i < 100; i++)
    {
        fprintf(fp, "%d\n", rand());
    }

    rewind(fp);
    int x;
    while (fscanf(fp, "%d", &x) != EOF)
    {
        printf("%d\t", x);
    }

    return 0;
}