#include <stdio.h>

int main()
{
    FILE *fp = fopen("testfile.txt", "w+");

    fprintf(fp, "Hello, my name is Justin"); //Intentional typo.

    fseek(fp, 18, SEEK_SET);

    fprintf(fp, "A");

    printf("SEEK_SET=%d, SEEK_CUR=%d, SEEK_END=%d\n", SEEK_SET, SEEK_CUR, SEEK_END);
    return 0;
}