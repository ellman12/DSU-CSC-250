#include <stdio.h>

int main()
{
    FILE *fp = fopen("testfile.txt", "a");

    fprintf(fp, "Hello, my name is ");

    fpos_t savedPos;
    fgetpos(fp, &savedPos);
    fprintf(fp, "Justin");

    //Do other stuff.
    fsetpos(fp, &savedPos);
    fprintf(fp, "A");
    return 0;
}