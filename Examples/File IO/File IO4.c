#include <stdio.h>

int main()
{
    FILE *fp = fopen("numbers.dat", "r");

    int sum = 0;
    int i, x;

    for (i = 0; i < 4; i++)
    {
        fscanf(fp, "%d", &x);
        sum += x;
    }

    printf("The sum is %d.\n", sum);
}