#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int main()
{
    FILE *fp = fopen("binary.bin", "wb+");

    char name[8] = "Elliott";
    int array[100];

    for (int i = 0; i < 100; i++)
        array[i] = rand();

    fwrite(array, sizeof(int), 100, fp);
    fwrite(name, sizeof(char), strlen(name) + 1, fp);

    rewind(fp);

    int x;

    for (int i = 0; i < 100; i++) {
        fread(&x, sizeof(int), 1, fp);
        printf("x is %d\t", x);
    }

    return 0;
}