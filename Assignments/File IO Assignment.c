#include <stdio.h>
#include <stdlib.h>

int main()
{
    FILE *binFilePtr = fopen("balances.bin", "rb");

    float x;
    while ((fread(&x, sizeof(float), 60, binFilePtr)) != 0)
    {
        printf("x: %f", x);
    }

    fclose(binFilePtr);

    return 0;
}